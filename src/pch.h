template <typename FuncPtr, typename... Args>
inline auto _call(std::uintptr_t address, Args... args)
{
	return reinterpret_cast<FuncPtr>(address)(std::forward<Args>(args)...);
}
template <typename T>
inline T _rv(std::uintptr_t base, std::ptrdiff_t offset = 0)
{
	return *reinterpret_cast<T*>(base + offset);
}

template <typename T>
inline void _wv(std::uintptr_t base, std::ptrdiff_t offset, T value)
{
	*reinterpret_cast<T*>(base + offset) = value;
}

