class SwapAddressPatch
{
public:
	SwapAddressPatch() = default;

	template <typename T>
	bool Patch(std::uintptr_t targetAddress, T* newValue, const std::uint32_t startOffs = 2)
	{
		start_offset = startOffs;
		m_targetAddress = reinterpret_cast<void*>(targetAddress);
		if (!m_targetAddress) return false;

		auto newValueAddress = reinterpret_cast<std::uintptr_t>(newValue);
		DWORD oldProtect;
		if (!VirtualProtect(m_targetAddress, start_offset + sizeof(std::uintptr_t), PAGE_EXECUTE_READWRITE, &oldProtect)) return false;
		m_originalBytes.resize(sizeof(std::uintptr_t));
		std::memcpy(m_originalBytes.data(), m_targetAddress, m_originalBytes.size());
		*reinterpret_cast<std::uintptr_t*>(reinterpret_cast<std::uintptr_t>(m_targetAddress) + start_offset) = newValueAddress;
		VirtualProtect(m_targetAddress, start_offset + sizeof(std::uintptr_t), oldProtect, &oldProtect);
		return true;
	}

	bool Unpatch()
	{
		if (!m_targetAddress || m_originalBytes.empty()) return false;
		DWORD oldProtect;
		if (!VirtualProtect(m_targetAddress, start_offset + m_originalBytes.size(), PAGE_EXECUTE_READWRITE, &oldProtect)) return false;
		std::memcpy(m_targetAddress, m_originalBytes.data(), m_originalBytes.size());
		VirtualProtect(m_targetAddress, start_offset + m_originalBytes.size(), oldProtect, &oldProtect);
		return true;
	}

private:
	void* m_targetAddress = nullptr;
	std::uint32_t start_offset = 2;
	std::vector<std::uint8_t> m_originalBytes;
};


